package modelo;


public class Rol {
	private String nombre;

	
	public Rol(){
		super();
	}
	
	public Rol(String nombre) {
		super();
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


}
